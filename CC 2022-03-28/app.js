// Very simple, given an integer or a floating-point number, find its opposite.

// Examples:

// 1: -1
// 14: -14
// -34: 34

function opposite(number) {
    //using math.sign() to determine if number is negative 
    if (Math.sign(number) === -1){
      return number * -1
    }else{
      return number * -1
    }
  }